static uint8_t gf_mul(uint8_t a, uint8_t b) {
    if (!a || !b) return 0;
    return RIJNDAEL_EXP[(RIJNDAEL_LOG[a] + RIJNDAEL_LOG[b]) % 0xFF];
}

static void xor_block(uint8_t *dst, const uint8_t *a, const uint8_t *b) {
    for (int i = 0; i < BLOCK_SIZE; i++) dst[i] = a[i] ^ b[i];
}

static void sub_bytes(uint8_t *state) {
    for (int i = 0; i < BLOCK_SIZE; i++) state[i] = SBOX[state[i]];
}

static void sub_bytes_inv(uint8_t *state) {
    for (int i = 0; i < BLOCK_SIZE; i++) state[i] = SBOX_INV[state[i]];
}

static void shift_rows(uint8_t *state) {
    uint8_t tmp[BLOCK_SIZE];
    for (int c = 0; c < 4; c++) {
        for (int r = 0; r < 4; r++) {
            tmp[c * 4 + r] = state[((c + r) & 3) * 4 + r];
        }
    }
    memcpy(state, tmp, BLOCK_SIZE);
}

static void shift_rows_inv(uint8_t *state) {
    uint8_t tmp[BLOCK_SIZE];
    for (int c = 0; c < 4; c++) {
        for (int r = 0; r < 4; r++) {
            tmp[((c + r) & 3) * 4 + r] = state[c * 4 + r];
        }
    }
    memcpy(state, tmp, BLOCK_SIZE);
}

static void mix_columns(uint8_t *state) {
    uint8_t tmp[4];
    for (int c = 0; c < 4; c++) {
        int i = c * 4;
        for (int r = 0; r < 4; r++) {
            tmp[r] = gf_mul(MIX_COLUMN_MATRIX[r][0], state[i])
                   ^ gf_mul(MIX_COLUMN_MATRIX[r][1], state[i + 1])
                   ^ gf_mul(MIX_COLUMN_MATRIX[r][2], state[i + 2])
                   ^ gf_mul(MIX_COLUMN_MATRIX[r][3], state[i + 3]);
        }
        memcpy(state + i, tmp, 4);
    }
}

static void mix_columns_inv(uint8_t *state) {
    uint8_t tmp[4];
    for (int c = 0; c < 4; c++) {
        int i = c * 4;
        for (int r = 0; r < 4; r++) {
            tmp[r] = gf_mul(MIX_COLUMN_MATRIX_INV[r][0], state[i])
                   ^ gf_mul(MIX_COLUMN_MATRIX_INV[r][1], state[i + 1])
                   ^ gf_mul(MIX_COLUMN_MATRIX_INV[r][2], state[i + 2])
                   ^ gf_mul(MIX_COLUMN_MATRIX_INV[r][3], state[i + 3]);
        }
        memcpy(state + i, tmp, 4);
    }
}

static void key_expansion(const uint8_t *key, int key_len, uint8_t *expanded) {
    int nk = key_len / 4;
    int nr = nk + 6;
    int exp_len = BLOCK_SIZE * (nr + 1);

    memcpy(expanded, key, key_len);

    int rcon_iter = 1;
    for (int i = nk; i < exp_len / 4; i++) {
        uint8_t temp[4];
        memcpy(temp, expanded + (i - 1) * 4, 4);
        if (i % nk == 0) {
            uint8_t t = temp[0];
            temp[0] = SBOX[temp[1]] ^ RCON[rcon_iter];
            temp[1] = SBOX[temp[2]];
            temp[2] = SBOX[temp[3]];
            temp[3] = SBOX[t];
            rcon_iter++;
        } else if (nk > 6 && i % nk == 4) {
            for (int j = 0; j < 4; j++) temp[j] = SBOX[temp[j]];
        }
        for (int j = 0; j < 4; j++)
            expanded[i * 4 + j] = expanded[(i - nk) * 4 + j] ^ temp[j];
    }
}

static void aes_encrypt_block(uint8_t *state, const uint8_t *expanded_key, int nr) {
    xor_block(state, state, expanded_key);
    for (int r = 1; r <= nr; r++) {
        sub_bytes(state);
        shift_rows(state);
        if (r != nr) mix_columns(state);
        xor_block(state, state, expanded_key + r * BLOCK_SIZE);
    }
}

static void aes_decrypt_block(uint8_t *state, const uint8_t *expanded_key, int nr) {
    xor_block(state, state, expanded_key + nr * BLOCK_SIZE);
    for (int r = nr - 1; r >= 0; r--) {
        shift_rows_inv(state);
        sub_bytes_inv(state);
        xor_block(state, state, expanded_key + r * BLOCK_SIZE);
        if (r != 0) mix_columns_inv(state);
    }
}

static void inc_block(uint8_t *block) {
    for (int i = BLOCK_SIZE - 1; i >= 0; i--) {
        if (++block[i]) break;
    }
}

static void shift_block(uint8_t *block) {
    int bit = 0;
    for (int i = 0; i < BLOCK_SIZE; i++) {
        int new_bit = block[i] & 1;
        block[i] = (block[i] >> 1) | (bit ? 0x80 : 0);
        bit = new_bit;
    }
}

static void block_product(uint8_t *z, const uint8_t *x, const uint8_t *y) {
    uint8_t v[BLOCK_SIZE];
    memcpy(v, y, BLOCK_SIZE);
    memset(z, 0, BLOCK_SIZE);
    uint8_t r[BLOCK_SIZE] = {0xE1, 0};

    for (int i = 0; i < BLOCK_SIZE; i++) {
        for (int bit = 7; bit >= 0; bit--) {
            if (x[i] & (1 << bit)) {
                xor_block(z, z, v);
            }
            int do_xor = v[BLOCK_SIZE - 1] & 1;
            shift_block(v);
            if (do_xor) xor_block(v, v, r);
        }
    }
}

static void ghash(uint8_t *result, const uint8_t *subkey, const uint8_t *data, Py_ssize_t len) {
    memset(result, 0, BLOCK_SIZE);
    for (Py_ssize_t i = 0; i < len; i += BLOCK_SIZE) {
        uint8_t block[BLOCK_SIZE];
        xor_block(block, result, data + i);
        block_product(result, block, subkey);
    }
}

static PyObject *_aes_cbc_decrypt_bytes(PyObject *self, PyObject *args) {
    const uint8_t *data, *key, *iv;
    Py_ssize_t data_len, key_len, iv_len;

    if (!PyArg_ParseTuple(args, "y#y#y#", &data, &data_len, &key, &key_len, &iv, &iv_len))
        return NULL;

    if (key_len != 16 && key_len != 24 && key_len != 32) {
        PyErr_SetString(PyExc_ValueError, "key must be 16, 24, or 32 bytes");
        return NULL;
    }
    if (iv_len != BLOCK_SIZE) {
        PyErr_SetString(PyExc_ValueError, "iv must be 16 bytes");
        return NULL;
    }

    int nr = (key_len / 4) + 6;
    uint8_t expanded_key[240];
    key_expansion(key, key_len, expanded_key);

    int block_count = (data_len + BLOCK_SIZE - 1) / BLOCK_SIZE;
    uint8_t *result = (uint8_t *)PyMem_Malloc(data_len);
    if (!result) return PyErr_NoMemory();

    uint8_t prev_cipher[BLOCK_SIZE];
    memcpy(prev_cipher, iv, BLOCK_SIZE);

    for (int i = 0; i < block_count; i++) {
        int offset = i * BLOCK_SIZE;
        int remaining = data_len - offset;
        int copy_size = remaining < BLOCK_SIZE ? remaining : BLOCK_SIZE;
        uint8_t block[BLOCK_SIZE];
        memcpy(block, data + offset, copy_size);
        if (copy_size < BLOCK_SIZE) memset(block + copy_size, 0, BLOCK_SIZE - copy_size);

        aes_decrypt_block(block, expanded_key, nr);
        xor_block(block, block, prev_cipher);
        memcpy(prev_cipher, data + offset, copy_size);
        memcpy(result + offset, block, copy_size);
    }

    PyObject *ret = PyBytes_FromStringAndSize((const char *)result, data_len);
    PyMem_Free(result);
    return ret;
}

static PyObject *_aes_ctr_decrypt(PyObject *self, PyObject *args) {
    const uint8_t *data, *key, *iv;
    Py_ssize_t data_len, key_len, iv_len;

    if (!PyArg_ParseTuple(args, "y#y#y#", &data, &data_len, &key, &key_len, &iv, &iv_len))
        return NULL;

    if (key_len != 16 && key_len != 24 && key_len != 32) {
        PyErr_SetString(PyExc_ValueError, "key must be 16, 24, or 32 bytes");
        return NULL;
    }
    if (iv_len != BLOCK_SIZE) {
        PyErr_SetString(PyExc_ValueError, "iv must be 16 bytes");
        return NULL;
    }

    int nr = (key_len / 4) + 6;
    uint8_t expanded_key[240];
    key_expansion(key, key_len, expanded_key);

    int block_count = (data_len + BLOCK_SIZE - 1) / BLOCK_SIZE;
    uint8_t *result = (uint8_t *)PyMem_Malloc(data_len);
    if (!result) return PyErr_NoMemory();

    uint8_t counter[BLOCK_SIZE];
    memcpy(counter, iv, BLOCK_SIZE);

    for (int i = 0; i < block_count; i++) {
        int offset = i * BLOCK_SIZE;
        int remaining = data_len - offset;
        int copy_size = remaining < BLOCK_SIZE ? remaining : BLOCK_SIZE;

        uint8_t enc_counter[BLOCK_SIZE];
        memcpy(enc_counter, counter, BLOCK_SIZE);
        aes_encrypt_block(enc_counter, expanded_key, nr);
        for (int j = 0; j < copy_size; j++)
            result[offset + j] = data[offset + j] ^ enc_counter[j];

        inc_block(counter);
    }

    PyObject *ret = PyBytes_FromStringAndSize((const char *)result, data_len);
    PyMem_Free(result);
    return ret;
}

static PyObject *_aes_gcm_decrypt_and_verify_bytes(PyObject *self, PyObject *args) {
    const uint8_t *data, *key, *tag, *nonce;
    Py_ssize_t data_len, key_len, tag_len, nonce_len;

    if (!PyArg_ParseTuple(args, "y#y#y#y#", &data, &data_len, &key, &key_len, &tag, &tag_len, &nonce, &nonce_len))
        return NULL;

    if (key_len != 16 && key_len != 24 && key_len != 32) {
        PyErr_SetString(PyExc_ValueError, "key must be 16, 24, or 32 bytes");
        return NULL;
    }
    if (tag_len != BLOCK_SIZE) {
        PyErr_SetString(PyExc_ValueError, "tag must be 16 bytes");
        return NULL;
    }

    int nr = (key_len / 4) + 6;
    uint8_t expanded_key[240];
    key_expansion(key, key_len, expanded_key);

    uint8_t zero_block[BLOCK_SIZE] = {0};
    uint8_t hash_subkey[BLOCK_SIZE];
    memcpy(hash_subkey, zero_block, BLOCK_SIZE);
    aes_encrypt_block(hash_subkey, expanded_key, nr);

    uint8_t j0[BLOCK_SIZE];
    if (nonce_len == 12) {
        memcpy(j0, nonce, 12);
        j0[12] = 0; j0[13] = 0; j0[14] = 0; j0[15] = 1;
    } else {
        int pad = (BLOCK_SIZE - (nonce_len % BLOCK_SIZE)) % BLOCK_SIZE + 8;
        Py_ssize_t ghash_in_len = nonce_len + pad + 8;
        uint8_t *ghash_in = (uint8_t *)PyMem_Malloc(ghash_in_len);
        if (!ghash_in) return PyErr_NoMemory();
        memcpy(ghash_in, nonce, nonce_len);
        memset(ghash_in + nonce_len, 0, pad);
        uint64_t bit_len = (uint64_t)nonce_len * 8;
        for (int i = 0; i < 8; i++)
            ghash_in[nonce_len + pad + i] = (bit_len >> (56 - i * 8)) & 0xFF;
        ghash(j0, hash_subkey, ghash_in, ghash_in_len);
        PyMem_Free(ghash_in);
    }

    uint8_t iv_ctr[BLOCK_SIZE];
    memcpy(iv_ctr, j0, BLOCK_SIZE);
    inc_block(iv_ctr);

    PyObject *ctr_args = Py_BuildValue("(y#y#y#)", data, data_len, key, key_len, iv_ctr, BLOCK_SIZE);
    PyObject *decrypted = _aes_ctr_decrypt(NULL, ctr_args);
    Py_DECREF(ctr_args);
    if (!decrypted) return NULL;

    int pad_len = (BLOCK_SIZE - (data_len % BLOCK_SIZE)) % BLOCK_SIZE;
    Py_ssize_t ghash_in_len = data_len + pad_len + 16;
    uint8_t *ghash_in = (uint8_t *)PyMem_Malloc(ghash_in_len);
    if (!ghash_in) { Py_DECREF(decrypted); return PyErr_NoMemory(); }
    memcpy(ghash_in, data, data_len);
    memset(ghash_in + data_len, 0, pad_len);
    memset(ghash_in + data_len + pad_len, 0, 8);
    uint64_t data_bit_len = (uint64_t)data_len * 8;
    for (int i = 0; i < 8; i++)
        ghash_in[data_len + pad_len + 8 + i] = (data_bit_len >> (56 - i * 8)) & 0xFF;

    uint8_t s_tag[BLOCK_SIZE];
    ghash(s_tag, hash_subkey, ghash_in, ghash_in_len);
    PyMem_Free(ghash_in);

    uint8_t tag_check[BLOCK_SIZE];
    memcpy(tag_check, j0, BLOCK_SIZE);
    aes_encrypt_block(tag_check, expanded_key, nr);
    for (int i = 0; i < BLOCK_SIZE; i++) tag_check[i] ^= s_tag[i];

    if (memcmp(tag_check, tag, BLOCK_SIZE) != 0) {
        Py_DECREF(decrypted);
        PyErr_SetString(PyExc_ValueError, "Mismatching authentication tag");
        return NULL;
    }

    return decrypted;
}

